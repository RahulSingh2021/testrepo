export function getAliases(source: 'FRANCE' | 'ITALY', name: string): string[] {
  const key = name.trim().toLowerCase();
  if (source === 'FRANCE') return FRENCH_ALIASES[key] || [];
  if (source === 'ITALY') return ITALIAN_ALIASES[key] || [];
  return [];
}

export function buildSearchHaystack(name: string, aliases: string[]): string {
  return (name + ' ' + aliases.join(' ')).toLowerCase();
}

export function decorateName(name: string, aliases: string[]): string {
  if (!aliases.length) return name;
  if (/[a-z]\s*\(/i.test(name) && /[a-z]\)/i.test(name)) return name;
  if (/[,—-]\s+[a-z]/i.test(name) && /\b(bread|cheese|cake|wine|sauce|pasta|rice|stew|soup|salad|fish|ham|sausage|cream|milk|butter|oil|coffee|water|tart|pancake|fries|mushroom|pepper|olive|grape|apple|pear|tomato|carrot|onion|spinach|lettuce|cured|pork|beef|veal|chicken|duck|liver|raw|cooked|fresh|dried|canned)\b/i.test(name)) return name;
  return `${name} (${aliases[0]})`;
}
